var message;

message = "Hello World!";
console.log(message);

message = 'Congratulations! Your tweet has won a prize...';
console.log(message);

// 1. Assign a third value and display it
message = "This is the third message!";
console.log(message);

// 2. Split the first message into two pieces and join them
message = "Hello " + "World!";
console.log(message);

// 3. Split the second message into three pieces and join them
message = 'Congratulations! ' + 'Your tweet ' + 'has won a prize...';
console.log(message);
